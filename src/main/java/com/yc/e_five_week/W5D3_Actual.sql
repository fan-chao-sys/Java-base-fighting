-- =====================================================
-- 一、环境准备：创建业务测试表 + 设计联合索引
-- 业务场景：订单表（高频查询场景）
-- 常用查询条件：用户ID、订单状态、创建时间，常查询订单金额
-- 联合索引设计：idx_uid_status_create (user_id, order_status, create_time)
-- 索引设计考量：
-- 1. 遵循最左前缀原则，把【等值查询、区分度高】字段放前面：user_id(用户维度筛选最多)
-- 2. 中间放等值字段 order_status（订单状态固定枚举，业务常搭配用户筛选）
-- 3. 末尾放范围字段 create_time（时间范围查询，范围后索引列失效，放最后最优）
-- 4. 覆盖常用查询条件，兼顾业务SQL，减少回表与索引失效问题
-- =====================================================
DROP TABLE IF EXISTS `order_info`;
CREATE TABLE `order_info` (
                              id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '订单主键(聚簇索引)',
                              user_id INT NOT NULL COMMENT '用户ID',
                              order_status TINYINT NOT NULL COMMENT '订单状态 0-待付款 1-已付款 2-已取消',
                              create_time DATETIME NOT NULL COMMENT '订单创建时间',
                              order_amount DECIMAL(10,2) NOT NULL COMMENT '订单金额',
                              pay_time DATETIME COMMENT '支付时间'
) ENGINE = InnoDB DEFAULT CHARSET = utf8mb4 COMMENT '订单业务表-联合索引规则测试';

