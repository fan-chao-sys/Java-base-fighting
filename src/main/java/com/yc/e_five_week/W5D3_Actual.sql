-- 创建联合索引 (user_id, order_status, create_time)
CREATE INDEX idx_uid_status_create ON order_info(user_id, order_status, create_time);

-- =====================================================
-- 联合索引规则前置说明
-- 索引顺序：user_id(列1) > order_status(列2) > create_time(列3)
-- 核心规则：
-- 1. 最左前缀匹配：必须从索引最左列开始匹配，不能跳过前列
-- 2. 范围查询截断：> < >= <= BETWEEN 等范围条件，右侧索引列全部失效
-- 3. 函数/隐式类型转换、or、not、like %前缀 会导致索引失效
-- =====================================================

-- =====================================================
-- 测试SQL 1：全列等值匹配（完全命中联合索引，最优）
-- 规则：全值匹配，索引全部生效
-- EXPLAIN观察：type=ref，正常走索引
-- =====================================================
EXPLAIN
SELECT * FROM order_info
WHERE user_id = 1001 AND order_status = 1 AND create_time = '2026-01-01 10:00:00';

-- =====================================================
-- 测试SQL 2：匹配最左第一列（满足最左前缀，索引生效）
-- 规则：仅使用联合索引首列，符合最左前缀，索引生效
-- =====================================================
EXPLAIN
SELECT * FROM order_info WHERE user_id = 1001;

-- =====================================================
-- 测试SQL 3：跳过首列，直接使用第二列（索引失效）
-- 规则：违反最左前缀，带头字段缺失，整个联合索引失效，全表扫描
-- =====================================================
EXPLAIN
SELECT * FROM order_info WHERE order_status = 1;

-- =====================================================
-- 测试SQL 4：跳过前两列，直接使用第三列（索引失效）
-- 规则：严重违反最左前缀，索引完全失效
-- =====================================================
EXPLAIN
SELECT * FROM order_info WHERE create_time > '2026-01-01';

-- =====================================================
-- 测试SQL 5：使用 列1 + 列2（连续前缀，索引生效）
-- 规则：连续匹配最左两列，符合最左前缀，索引生效
-- =====================================================
EXPLAIN
SELECT * FROM order_info WHERE user_id = 1002 AND order_status = 0;

-- =====================================================
-- 测试SQL 6：列1等值 + 列2范围查询（范围截断，列3失效）
-- 规则：范围查询右侧索引列失效；此处列2是范围，列3无法使用索引
-- =====================================================
EXPLAIN
SELECT * FROM order_info
WHERE user_id = 1001 AND order_status > 0 AND create_time = '2026-01-02 11:00:00';

-- =====================================================
-- 测试SQL 7：列1等值 + 列2等值 + 列3范围（范围放最后，规范用法）
-- 规则：范围字段放在联合索引末尾，仅范围列本身截断，前列正常使用索引
-- =====================================================
EXPLAIN
SELECT * FROM order_info
WHERE user_id = 1002 AND order_status = 2 AND create_time > '2026-01-03';

-- =====================================================
-- 测试SQL 8：查询条件乱序（MySQL优化器自动调整顺序，索引仍生效）
-- 规则：WHERE条件顺序不影响，优化器会自动对齐联合索引顺序
-- =====================================================
EXPLAIN
SELECT * FROM order_info
WHERE order_status = 1 AND user_id = 1003;

-- =====================================================
-- 测试SQL 9：索引列使用函数运算（索引失效）
-- 规则：索引列参与函数、计算，无法走索引
-- =====================================================
EXPLAIN
SELECT * FROM order_info WHERE DATE(create_time) = '2026-01-01';

-- =====================================================
-- 测试SQL 10：LIKE 左模糊查询（%开头，索引失效）
-- 规则：前缀模糊匹配，联合索引失效；后缀模糊可走索引
-- =====================================================
EXPLAIN
SELECT * FROM order_info WHERE create_time LIKE '%10:00:00';

